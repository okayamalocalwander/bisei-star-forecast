"""毎年おおよそ同じ時期に起こる天文イベント（主要な流星群の極大日）の簡易カレンダー。

年による1日程度のズレは許容し、実運用では国立天文台の暦要項等での年次更新を想定する。
"""
import datetime

# (月, 日, イベント名)
RECURRING_EVENTS = [
    (1, 3, "しぶんぎ座流星群 極大"),
    (1, 4, "しぶんぎ座流星群 極大"),
    (4, 22, "こと座流星群 極大"),
    (5, 5, "みずがめ座η流星群 極大"),
    (5, 6, "みずがめ座η流星群 極大"),
    (8, 12, "ペルセウス座流星群 極大"),
    (8, 13, "ペルセウス座流星群 極大"),
    (10, 21, "オリオン座流星群 極大"),
    (11, 17, "しし座流星群 極大"),
    (12, 13, "ふたご座流星群 極大"),
    (12, 14, "ふたご座流星群 極大"),
]


def get_events_for_date(date: datetime.date) -> list:
    return [name for (m, d, name) in RECURRING_EVENTS if m == date.month and d == date.day]
